# Numbers in python -> int, float, complex

# Decimal Division '/' -> accurate, includes decimals
print(17 / 8)

# Integer Divison '//' -> doesn't include decimals/fractions/remainder if there are any
print(17 // 8)

# Complex numbers
# cannot use i to represent imaginary number -> special character for formatting
complex = 1+2j+5+3j
print(complex)

# Calculate exponents (base ** exponent) -> base ^exponent
# 2^3 2 to the power of 3 = 8
print(2 ** 3)

# modulo % operator -> returns remainder of division oepration
print(21 % 2)
# should be 10 remainder 1
# will just return remainder = 1
